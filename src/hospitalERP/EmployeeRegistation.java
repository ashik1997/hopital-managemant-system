    private void tbl_employeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_employeeMouseClicked
        try {
        int row=tbl_employee.getSelectedRow();
        String tableClick=(tbl_employee.getModel().getValueAt(row, 0).toString());
        String sql="select * from EmployeeTable  where Employee_ID ='"+tableClick+"'";
        pst=conn.prepareStatement(sql);
        rs=pst.executeQuery();
        if(rs.next()){
        txt_employee_id.setText(rs.getString("Employee_ID"));
        txt_employee_name.setText(rs.getString("Employee_Name"));
        cmb_gender.setSelectedItem(rs.getString("Gender"));
        txt_religion.setText(rs.getString("Religion"));
        txt_nid.setText(rs.getString("NID"));
        txt_birthday.setText(rs.getString("Birthday"));        
        txt_phone.setText(rs.getString("Phone"));
        txt_email.setText(rs.getString("Email"));
        txt_title.setText(rs.getString("Titel"));
        txt_department.setText(rs.getString("Depatment"));
        txt_work_location.setText(rs.getString("Work_Location"));        
        txt_start_date.setText(rs.getString("Start_Date"));
        txt_salary.setText(rs.getString("Salary"));
        txt_address.setText(rs.getString("Address"));
        txt_addressp.setText(rs.getString("PAddress"));
        txt_doctor_details.setText(rs.getString("Doctor_Detalies"));        
        txt_visit_amount.setText(rs.getString("Visite_Amount"));
        txt_office_phone.setText(rs.getString("Office_Phone"));
        cmb_maritalstatus.setSelectedItem(rs.getString("Marital_Status"));
        //---image set
        byte[] person_imag=rs.getBytes("Image");
        ImageIcon imageicon = new ImageIcon(new ImageIcon(person_imag).getImage().getScaledInstance(lbl_image.getWidth(), lbl_image.getHeight(), Image.SCALE_DEFAULT));
        lbl_image.setIcon(imageicon);        
        
        }
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null,e);
        }finally {
            try {
                rs.close();
                pst.close();
            }catch (SQLException e) {}
        }
    }//GEN-LAST:event_tbl_employeeMouseClicked


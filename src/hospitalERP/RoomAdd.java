    private void tbl_roomMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_roomMouseClicked
        try{
        int row=tbl_room.getSelectedRow();
        String tableClick=(tbl_room.getModel().getValueAt(row, 0).toString());
        String sql="select * from Room where RID='"+tableClick+"' ";
        pst=conn.prepareStatement(sql);
        rs=pst.executeQuery();
        if(rs.next()){
        txt_rid.setText(rs.getString("RID"));
        txt_department.setText(rs.getString("Department"));
        txt_bulding.setText(rs.getString("Bulding"));        
        txt_floor.setText(rs.getString("Floor"));
        txt_word.setText(rs.getString("Ward"));
        txt_room.setText(rs.getString("Room"));
        txt_bed.setText(rs.getString("Bed"));
        txt_type.setText(rs.getString("Type"));
        txt_price.setText(rs.getString("Price"));
        }                        
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null,e);

        }  finally{
        try{
        rs.close();
        pst.close();
        }catch(Exception e){}
        }
    }//GEN-LAST:event_tbl_roomMouseClicked


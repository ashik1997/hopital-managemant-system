     public final void AutoRID(){
        try{
            String sl="select max(RID) from Room";
            pst=conn.prepareStatement(sl);
            rs=pst.executeQuery();
            String s=rs.getString("max(RID)");
            if(s==null){
            txt_rid.setText("1"); 
            }else{
            int a = Integer.parseInt(s);
            int b=a+1;
            txt_rid.setText(String.valueOf(b));
            }
            
        }catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
        try{
            rs.close();
            pst.close();
        }catch(SQLException e){}
        }
     }
     

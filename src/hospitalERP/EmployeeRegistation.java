public final void AutoEmployeeID(){
        try{
            String sl="select max(Employee_ID) from EmployeeTable";
            pst=conn.prepareStatement(sl);
            rs=pst.executeQuery();
            String s=rs.getString("max(Employee_ID)");
            if(s==null){
            txt_employee_id.setText("1"); 
            }else{
            int a = Integer.parseInt(s);
            int b=a+1;
            txt_employee_id.setText(String.valueOf(b));
            }
            
        }catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
        try{
            rs.close();
            pst.close();
        }catch(SQLException e){}
        }
         } 

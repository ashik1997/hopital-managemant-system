    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        try{
            String sql1 = "select * from Room where RID=?";
            pst=conn.prepareStatement(sql1);
            pst.setString(1, txt_rid.getText());
            rs=pst.executeQuery();
            if(rs.next()){
            JOptionPane.showMessageDialog(null, "Already Exist");
            }else{
            
        if("".equals(txt_rid.getText())){
            AutoRID();
        }else{
        
        try {
            String sql = "insert into Room (RID,Department,Bulding,Floor,Ward,Room,Type,Price,Entered_By,Bed) values (?,?,?,?,?,?,?,?,?,?)";
            
            pst = conn.prepareStatement(sql);
            pst.setString(1, txt_rid.getText());
            pst.setString(2, txt_department.getText());
            pst.setString(3, txt_bulding.getText());
            pst.setString(4, txt_floor.getText());
            pst.setString(5, txt_word.getText());
            pst.setString(6, txt_room.getText());         
            pst.setString(7, txt_type.getText());
            pst.setString(8, txt_price.getText());
            pst.setString(9, loginby);
            pst.setString(10, txt_bed.getText());
            pst.execute();
            TextFealdClear();
            AutoRID();
            RoomUpdateTable();
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                rs.close();
                pst.close();
            }catch(SQLException e){}
        }
        }
            }
        }catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{
                rs.close();
                pst.close();
            }catch(SQLException e){}
        }
        
    }//GEN-LAST:event_btn_addActionPerformed

